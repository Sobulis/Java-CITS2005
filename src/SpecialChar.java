public class SpecialChar {
    public static void main(String[] args){
        char tab = '\t';
        System.out.println("Hello"+tab+"World"); //Hello   World
        String s = "backslash: \\, double quote: \", single quote: \'"; //backslash: \, double quote: ", single quote: '
        System.out.println(s+"\n"+"another line!"); // another line!
    }
}
