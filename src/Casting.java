public class Casting {
    public static void main(String[] args){
        double d = 66.3;
        int num = (int)d;
        char c = (char) num;
        long l = 11123456789L;
        System.out.println("num="+num); //num = 66
        System.out.println("c="+c); //c = B
        System.out.println("l="+l); // l = 11123456789

    }
}
